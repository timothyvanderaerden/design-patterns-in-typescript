export class CdPlayer {
  public on(): void {
    console.log('The CdPlayer is on');
  }

  public off(): void {
    console.log('The CdPlayer is off');
  }

  public eject(): void {
    console.log('Eject the CD');
  }

  public pause(): void {
    console.log('Pause the CD');
  }

  public play(): void {
    console.log('Play the CD');
  }

  public stop(): void {
    console.log('Stop the CD');
  }
}
