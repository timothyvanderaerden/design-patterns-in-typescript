export class Projector {
  public on(): void {
    console.log('The Projector is on');
  }

  public off(): void {
    console.log('The Projector is off');
  }

  public tvMode(): void {
    console.log('The Projector is now in TV-mode');
  }

  public wideScreenMode(): void {
    console.log('The Projector is now in WideScreen-mode');
  }
}
